char	*ft_strcat(char *dest, char *src)
{
	int i;
	int i3;

	i = -1;
	i3 = -1;
	while (dest[++i3])
		;
	while (src[++i])
		dest[i3 + i] = src[i];
	dest[i3 + i] = '\0';
	return (dest);
}
