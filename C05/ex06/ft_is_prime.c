int	ft_is_prime(int nb)
{
	int i;

	if (nb < 2)
		return (0);
	if (nb == 2 || nb == 3 || nb == 5 || nb == 7 || nb == 11)
		return (1);
	i = 10;
	while (++i && nb % 2 && nb % 3 && nb % 5 && nb % 7 && nb % i)
		if (i > (nb / i))
			return (1);
	return (0);
}
